    @RequestMapping(value = "/uploadimage", method = RequestMethod.POST, produces = MediaType.APPLICATION_JSON_VALUE)
    @ResponseBody
    public Map<String,Object> save(HttpServletRequest req){
        Map<String,Object> rs = new HashMap<String, Object>();
        MultipartHttpServletRequest mReq  =  null;
        MultipartFile file = null;
        InputStream is = null ;
        String fileName = "";
        // 原始文件名   UEDITOR创建页面元素时的alt和title属性
        String originalFileName = "";
        //String filePath = "E:/test/";
        //String filePath = "/ueditor/jsp/upload/image/";
        try {
            mReq = (MultipartHttpServletRequest)req;
            // 从config.json中取得上传文件的ID
            file = mReq.getFile("upfile");            
            // 取得文件的原始文件名称
            fileName = file.getOriginalFilename();
            originalFileName = fileName;    
                                    
            //获取跟目录  
            File path = ResourceUtils.getFile("classpath:static");
            String pathString = java.net.URLDecoder.decode(path.getAbsolutePath(),"utf-8");            
            //如果上传目录为/static/images/upload/，则可以如下获取：            
            File upload = new File(pathString,"/ueditor/jsp/upload/image/");
                        
            // 解决中文问题，liunx下中文路径，图片显示问题
            // fileName = UUID.randomUUID() + suffixName;
            File dest = new File(upload,fileName);
            // 检测是否存在目录
            if (!dest.getParentFile().exists()) {
                dest.getParentFile().mkdirs();
            }
            try {
                file.transferTo(dest);                                               
            } catch (IllegalStateException e) {
                e.printStackTrace();
            }
            
            /*你的处理图片的代码*/
            rs.put("state", "SUCCESS");// UEDITOR的规则:不为SUCCESS则显示state的内容
            rs.put("url","http://192.168.1.136:8011/ueditor/jsp/upload/image/" + fileName);         //能访问到你现在图片的路径
            rs.put("title", originalFileName);
            rs.put("original", originalFileName);                       

        } catch (Exception e) {
            //log.error(e.getMessage(),e);
            rs.put("state", "文件上传失败!"); //在此处写上错误提示信息，这样当错误的时候就会显示此信息
            rs.put("url","");
            rs.put("title", "");
            rs.put("original", "");
        }
        return rs;
    }
